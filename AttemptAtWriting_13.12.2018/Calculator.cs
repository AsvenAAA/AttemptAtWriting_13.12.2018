using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace AttemptAtWriting_13._12._2018
{
    class Calculator
    {



        static double Add(double x, double y)
        {
            double res = x + y;
            return res;
        }

        static double Sub(double x, double y)
        {
            double res = x - y;
            return res;
        }

        static double Mult(double x, double y)
        {
            double res = x * y;
            return res;
        }

        static double Div(double x, double y)
        {
            double res = x / y;
            return res;
        }

    }
}